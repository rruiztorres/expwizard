<template>
  <v-app id="agexApp">
    <v-app-bar
      dark
      color="indigo accent-4" 
      app
    >
      <v-app-bar-nav-icon @click="drawer = !drawer"></v-app-bar-nav-icon>
      <v-toolbar-title>AGEX</v-toolbar-title>
      <v-spacer></v-spacer>
      <img src="@/assets/img/logo_web_IGN_CNIG.svg" id="icon">
    </v-app-bar>

    <v-navigation-drawer
      v-model="drawer"
      fixed
      temporary
    >
      <UserMenu
        @activeMenu="activeOption"
      ></UserMenu>
    </v-navigation-drawer>  
      
    <v-main class="grey lighten-2 mainApp"> 
      <!-- MENU MIS EXPEDIENTES -->
      <v-container v-if="active === 'misExpedientes'">
        <v-row class="rowWrapper">
          <v-col cols="12">
            <MisExpedientes
            @edit="activeOption"
            @dataEdit="getData"
            ></MisExpedientes>
          </v-col>
        </v-row>
      </v-container>

      <!-- SELECTOR DE TIPOS EXPEDIENTE -->   
      <v-container v-if="active === 'selector'">
        <v-row class="rowWrapper">
          <v-col cols="12">
            <Selector
              @tipoExp="activeOption"
            ></Selector>
          </v-col>
        </v-row>
      </v-container>

      <!-- WIZARDS -->
      <v-container v-if="active === 'Suministro abierto varios criterios'">
        <v-row class="rowWrapper">
          <v-col cols="12">
            <SuministroAbierto 
              @back="activeOption" 
              :dataInput="loadData"
            ></SuministroAbierto>
          </v-col>
        </v-row>
      </v-container>     
    </v-main>
  </v-app>
</template>

<script>
import UserMenu from "@/components/common/UserMenu"
import SuministroAbierto from "@/components/SuministroAbierto/SuministroAbierto"


import Selector from "@/components/common/Selector"
import MisExpedientes from "@/components/common/MisExpedientes"

  export default {
    components: {
      UserMenu, 
      SuministroAbierto,
      Selector,
      MisExpedientes,
    },

    data () {
        return {
            drawer: null,
            active: 'selector',
            loadData: undefined,
        }
    },

    watch:{
      active(){
        //EVITA LA PERMANENCIA DE DATOS GUARDADOS
        if(this.active === 'selector'){
          this.loadData = undefined;
        }
      }
    },

    methods:{
      activeOption(option){
        this.active = option;
        this.drawer = false;
      },

      getData(data){
        this.loadData = data;
        this.selector = this.loadData.tipo;
      }

      
    }
  }
</script>

